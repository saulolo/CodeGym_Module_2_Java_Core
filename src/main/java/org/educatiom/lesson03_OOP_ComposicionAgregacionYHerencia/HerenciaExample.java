public class HerenciaExample {

    public static void main(String[] args) {

        System.out.println("--------------------- Herencia ---------------------------");
        //Herencia
        /*En Java, simplemente puede declarar la otra clase como el padre de su clase. Esto es equivalente a agregar el
        código de esa clase a su propia clase. Todos los datos y métodos de la clase principal aparecerán en su clase.
        Por ejemplo, podrías heredar de un «caballo», agregarle «alas» y obtener un «Pegaso».
        La herencia también tiene otros usos. Suponga que tiene diez clases que son muy similares. Tienen datos y
        methods coincidentes. Podría crear una clase base especial , mover los datos (y los methods asociados) a la
        clase base y tener esas diez clases heredar de él. En otras palabras, para cada clase, usted indica que tiene
        una clase principal, también conocida como clase base o super clase*/

        /*Las clases de Java no admiten la herencia múltiple de implementación: una clase solo puede tener una sola
        clase principal. Pero hay herencia múltiple de tipo, lo que significa que una clase puede implementar más de una
        interfaz. Esto mitiga ligeramente el problema.*/


    }
}
